import mongoose from 'mongoose';

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

// Add an index to ensure email uniqueness
UserSchema.index({ email: 1 }, { unique: true });

const User = mongoose.model('users', UserSchema);

export default User;